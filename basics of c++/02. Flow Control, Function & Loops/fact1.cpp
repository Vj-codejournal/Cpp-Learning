#include<bits/stdc++.h>
#include<iostream>

/*You can't really compare the two. iostream is a header file that allows you to use input (cin) and output (cout).
 A header file is basically just a file with a collection of functions you can use to make coding easier.
This is similar to the built in library in Python (Ex: import random).
bits/stdc++.h is basically a way to import every single C++ header file.
Many competitive programmers use this because they don't have to import every popular header file.
 However, a disadvantage is that it increases compilation time because it has to search through every possible functions.
 */